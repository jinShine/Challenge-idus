import UIKit

struct Color {
  
  var dark: UIColor {
    return UIColor(r: 20, g: 20, b: 40)
  }
  
  var blueGrey: UIColor {
    return UIColor(r: 171, g: 171, b: 196)
  }
  
  var darkBlueGrey: UIColor {
    return UIColor(r: 24, g: 24, b: 90, a: 0.04)
  }
  
  var paleGrey: UIColor {
    return UIColor(r: 246, g: 246, b: 250)
  }
  
  var darkNavyBlue: UIColor {
    return UIColor(r: 0, g: 0, b: 10, a: 0.36)
  }
  
}
